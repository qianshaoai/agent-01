import type { KbSearchResult } from "@/lib/kb/types";

const KB_NO_RELEVANT_ANSWER = "知识库中没有找到相关资料，无法回答该问题。";
const KB_UNAVAILABLE_ANSWER = "知识库暂时不可用，无法基于知识库回答该问题。";

/**
 * RAG 注入给模型的硬规则。
 * 目标：绑定知识库后只按检索资料回答，不再补“常识答案”。
 */
export function buildKbStrictAnswerPrompt(chunks: KbSearchResult[]): string {
  if (chunks.length === 0) {
    return (
      "【知识库权威性 · 硬性规则，不可违反】\n" +
      "本智能体已绑定知识库，但本轮没有检索到与问题相关的资料。\n\n" +
      "规则（无条件适用，违反任何一条即错误回答）：\n" +
      "① 只允许依据知识库资料回答，不允许使用训练常识、世界知识或个人推断补充事实。\n" +
      `② 当前没有可用资料支撑答案时，必须直接回复：「${KB_NO_RELEVANT_ANSWER}」\n` +
      "③ 不要编造、推测、纠正常识，也不要输出任何知识库外补充说明。\n\n" +
      "—— 以下是本轮用户问题（请按上述硬规则作答）——\n"
    );
  }

  const refBlock = chunks
    .map((c, i) => `【资料 ${i + 1}】\n${c.content}`)
    .join("\n\n");

  return (
    "【知识库权威性 · 硬性规则，不可违反】\n" +
    "你正在使用一个内部知识库。下方「资料」就是事实，对资料覆盖的内容拥有最高权威。\n\n" +
    "规则（无条件适用，违反任何一条即错误回答）：\n" +
    "① 回答必须严格以「资料」为准，所有事实判断都以资料为最高依据。\n" +
    "② 即使资料与你的训练常识 / 世界知识 / 普遍认知严重冲突，**仍然以资料为准**。\n" +
    "   严禁出现以下任何形式的反驳/纠正/质疑：\n" +
    "   - 「实际上 / 事实上 / 通常来说 / 严格来说 / 准确地说 …」\n" +
    "   - 「这可能是误解 / 误传 / 错误信息 / 不准确 / 夸张的说法」\n" +
    "   - 「正确的应该是 / 正确版本是 / 我建议参考 …」\n" +
    "   - 「资料里说 X，但实际是 Y」「资料提到 X，然而 …」\n" +
    "   - 任何知识库外补充说明\n" +
    "③ 只允许使用「资料」中能够支撑的内容作答；资料未覆盖的事实、原因、建议、背景，一律不要补充常识或推测。\n" +
    `④ 如果「资料」不足以回答用户问题，必须直接说明：「${KB_NO_RELEVANT_ANSWER}」不要另起段补充常识。\n` +
    "⑤ 用户的问题即便与资料字面矛盾，仍按资料回答；不要解释「资料为什么不对」。\n\n" +
    "—— 资料 ——\n" +
    refBlock +
    "\n\n—— 以下是本轮用户问题（请按上述硬规则作答）——\n"
  );
}

export function buildKbUnavailablePrompt(): string {
  return (
    "【知识库权威性 · 硬性规则，不可违反】\n" +
    "本智能体已绑定知识库，但本轮知识库检索失败。\n\n" +
    "规则（无条件适用，违反任何一条即错误回答）：\n" +
    "① 不允许绕过知识库使用训练常识、世界知识或个人推断回答。\n" +
    `② 必须直接回复：「${KB_UNAVAILABLE_ANSWER}」\n` +
    "③ 不要编造、推测、纠正常识，也不要输出任何知识库外补充说明。\n\n" +
    "—— 以下是本轮用户问题（请按上述硬规则作答）——\n"
  );
}
